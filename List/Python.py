my_list = []
my_list.append(5)  # Adds 5 to the end of the list

# delete
my_list = [1, 2, 3, 4, 5]
my_list.remove(3)  # Removes the element with value 3
# or
del my_list[2]  # Removes the element at index 2

# search - get index
my_list = [1, 2, 3, 4, 5]
index = my_list.index(3)  # Returns the index of the element with value 3

# access
my_list = [1, 2, 3, 4, 5]
element = my_list[2]  # Gets the element at index 2, which is 3

